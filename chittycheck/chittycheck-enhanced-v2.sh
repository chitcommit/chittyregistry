# Parse arguments
while [[ $# -gt 0 ]]; do
    case $1 in
        --fix|-f)
            FIX_MODE=true
            shift
            ;;
        --verbose|-v)
            VERBOSE=true
            shift
            ;;
        --qa|-q)
            QA_MODE=true
            shift
            ;;
        --security|-s)
            SECURITY_MODE=true
            shift
            ;;
        --quick)
            QUICK_MODE=true
            shift
            ;;
        --help|-h)
            show_help
            exit 0
            ;;
        *)
            shift
            ;;
    esac
done

# ============================================
# HELPER FUNCTIONS
# ============================================

add_violation() {
    local description="$1"
    local fix_command="$2"
    local category="${3:-General}"

    VIOLATIONS+=("[$category] $description")
    if [ -n "$fix_command" ]; then
        FIX_COMMANDS+=("$fix_command")
    fi
}

add_warning() {
    local description="$1"
    local suggestion="${2:-}"
    WARNINGS+=("$description")
    [ -n "$suggestion" ] && WARNINGS+=("  â†’ $suggestion")
}

add_success() {
    local item="$1"
    SUCCESS_ITEMS+=("$item")
}

log_status() {
    local status="$1"
    local message="$2"
    local detail="${3:-}"

    case "$status" in
        "success")
            echo -e "${GREEN}âœ…${RESET} $message"
            add_success "$message"
            ;;
        "warning")
            echo -e "${YELLOW}âš ï¸${RESET}  $message"
            add_warning "$message" "$detail"
            ;;
        "error")
            echo -e "${RED}âŒ${RESET} $message"
            add_violation "$message" "$detail"
            ;;
        "info")
            echo -e "${CYAN}â„¹ï¸${RESET}  $message"
            ;;
        "fix")
            echo -e "${BLUE}ğŸ”§${RESET} $message"
            ;;
    esac
}

# ============================================
# AUTO-FIX FUNCTIONS
# ============================================

fix_env_file() {
    if [ ! -f ".env" ]; then
        cat > .env << 'EOF'
# ChittyID Service Configuration (REQUIRED)
CHITTY_ID_TOKEN=chitty-dev-token-2025

# ChittyOS Integration
CHITTYOS_ACCOUNT_ID=bbf9fcd845e78035b7a135c481e88541
EOF
        log_status "fix" "Created .env file with default configuration"
        return 0
    fi
    return 1
}

fix_gitignore() {
    if [ ! -f ".gitignore" ]; then
        cat > .gitignore << 'EOF'
node_modules/
.env
.env.local
.wrangler/
dist/
build/
*.log
.DS_Store
coverage/
.vscode/
.idea/
*.swp
*.swo
.cache/
EOF
        log_status "fix" "Created .gitignore with standard patterns"
        return 0
    elif ! grep -q "^.env$" .gitignore 2>/dev/null; then
        echo ".env" >> .gitignore
        log_status "fix" "Added .env to .gitignore"
        return 0
    fi
    return 1
}

